Most beginners try to memorise opening lines. That''s the wrong approach. Principles are *rules of thumb* that guide good play in any position — whether or not you''ve seen it before.

## 1. Control the Centre

The four central squares — **e4, d4, e5, d5** — are the most valuable on the board. Pieces in the centre radiate influence in all directions. Pieces stuck on the edges are weak.

**In practice:** Open with 1.e4 or 1.d4. Respond to 1.e4 with 1...e5 or 1...c5. Fight for central space.

## 2. Develop Your Pieces Early

Every move you spend moving an already-developed piece is a move your opponent uses to mobilise another piece. The player who finishes development first almost always attacks first.

**Rule:** In the opening, try to develop a new piece on each move. Avoid moving the same piece twice unless forced.

## 3. King Safety — Castle Early

The king is a liability in the middlegame. Castling tucks it behind a wall of pawns and connects your rooks. Delaying it is one of the most common amateur mistakes.

**Rule:** Castle within the first 10 moves. Don''t castle if it walks into an attack — but usually, castle as soon as you can.

## 4. Rooks Belong on Open Files

A rook on a closed file is nearly useless. Rooks need open files (no pawns blocking) to exert pressure. Place them where they can see the most of the board.

**In practice:** When pawns are exchanged and a file opens, place a rook on it immediately. Double rooks on an open file for maximum effect.

## 5. Every Move Must Have a Purpose

Beginner chess is full of *waiting moves* — shuffling pieces without a plan. Every move should either:

- Attack something
- Defend something
- Improve a piece''s position
- Create a threat

Ask yourself before each move: *"What does this move do?"* If you can''t answer, find a better move.

